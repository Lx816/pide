package www.example.liangmutian.mypicker;

public class TimeBean {
    private int currYear;
    private int currMonth;
    private int currDay;
    private int currHour;
    private int currMin;
    private int currSec;

    public int getCurrSec() {
        return currSec;
    }

    public void setCurrSec(int currSec) {
        this.currSec = currSec;
    }

    public int getCurrYear() {
        return currYear;
    }

    public void setCurrYear(int currYear) {
        this.currYear = currYear;
    }

    public int getCurrMonth() {
        return currMonth;
    }

    public void setCurrMonth(int currMonth) {
        this.currMonth = currMonth;
    }

    public int getCurrDay() {
        return currDay;
    }

    public void setCurrDay(int currDay) {
        this.currDay = currDay;
    }

    public int getCurrHour() {
        return currHour;
    }

    public void setCurrHour(int currHour) {
        this.currHour = currHour;
    }

    public int getCurrMin() {
        return currMin;
    }

    public void setCurrMin(int currMin) {
        this.currMin = currMin;
    }
}
